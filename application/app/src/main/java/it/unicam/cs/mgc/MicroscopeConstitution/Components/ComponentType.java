package it.unicam.cs.mgc.MicroscopeConstitution.Components;

/**
 * The ComponentType interface defines a method to get a description of the type of component.
 * It is used to provide detailed information about the type of component being represented.
 */

public interface ComponentType {

    /**
     * Returns the description of the component type.
     *
     * @return a String representing the description of the component type.
     */

    String getDescriptionType();
}
